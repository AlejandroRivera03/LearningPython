print( '{:>20}'.format('Hola Mundo') ) # right alignment with 20 chars
print( '{:.3}'.format('Hola Mundo') ) # Trucate at fourth char
print( '{:^20.1}'.format('Hola Mundo') ) # center alignment with 20 chars and trucate at second char
print( '{:05d}'.format(150) ) # 5 digits filling with zeros (0)
print( '{:7d}'.format(7887) ) # 7 digits filling with spaces ( )
print( '{:07.3f}'.format(20.02) ) # 3 ints and 3 decimals